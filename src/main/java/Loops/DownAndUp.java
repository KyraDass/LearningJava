public class DownAndUp {
    public static void main(String[] args) {
        for (int i = 40; i >= 0 ; i = i-2) {
            System.out.println( i + "");
            
        }
        for (int i = 0; i <= 40; i = i + 2) {
            System.out.println(i + "");;
            
        }
    }
}
